# ── Generator ─────────────────────────────────────────────────────────────────

def generate_dataset(seed: int = 42) -> list[SafetyRecord]:
    """
    Generate and return the full list of SafetyRecord instances.
    Deterministic: same seed always produces same UUIDs and timestamps.
    """
    random.seed(seed)
    logger.info("Generating dataset with seed=%d", seed)

    records: list[SafetyRecord] = []
    base_ts = datetime(2025, 1, 1, 0, 0, 0)

    for i, raw in enumerate(_RAW_RECORDS):
        # Deterministic UUID from record index
        record_uuid = str(uuid.UUID(int=random.getrandbits(128)))
        # Spread timestamps across 50 days
        ts = base_ts.replace(day=1 + i) if i < 28 else base_ts.replace(
            month=2, day=1 + (i - 28)
        )

        record = SafetyRecord(
            id=record_uuid,
            version="1.0.0",
            created_at=ts.isoformat() + "Z",
            source="synthetic",
            **raw,
        )
        records.append(record)
        logger.debug("Generated record %d: %s [%s]", i + 1, record.id, record.category)

    logger.info("Dataset generation complete: %d records", len(records))
    return records
